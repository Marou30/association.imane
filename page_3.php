        <p> 
<h3> Les Vertus Du Jeûne Sur Le Corps</h3>
Qui ne se réjouit pas chaque année de l’arrivée du mois de ramadan et de la miséricorde. En effet, outre ses bienfaits spirituels, ce mois comporte d’innombrables avantages sur le corps. Et comme le prophète (salla allahou alayh wa sallam) l’a dit il y a 14 siècles : <strong>« Jeûnez et vous serez en bonne santé ». A travers cet article, nous allons énumérer les vertus du jeûne sur l’organisme.</strong></p>

<h3>Equilibre de l’appareil digestif </h3>
<p>Personne ne peut nier aujourd’hui le pouvoir thérapeutique du jeûne surtout lorsqu’il est corrélé à la pratique spirituelle.<br/>

Pour les musulmans, l’abstinence de manger et/ou de boire, du lever au coucher de soleil et durant un mois permet au tube digestif de régénérer les tissus lésés et de se détoxifier.</p>

<p>Scientifiquement parlant, faire un repos digestif ne peut être que bénéfique, à condition que le jeûneur soit en bonne santé. Et c’est pour cette raison que l’Islam épargne les musulmans affectés par des malaises ou des maladies chroniques de jeûner.</p>

<h3>Autres bienfaits physiologiques</h3>
<p>Le corps du jeûneur bénéficie encore de plusieurs vertus, à titre d’exemple :</p>

<p>La baisse considérable du taux de cholestérol dans le sang et qui est considérée comme la première cause des troubles cardio-vasculaires.</p>

<p>Des études scientifiques ont décelé qu’une privation d’eau durant environ 8 heures/jour permet même de réanimer le fonctionnement du système rénal.</p>

<p>Le jeûne peut être également un catalyseur de la reconstruction cellulaire en stimulant certains métabolismes du corps.</p>

<p>Le foie peut être allégé suite à la réduction de la fonction biliaire et l’assimilation des glucides.</p>

<p>Que dit le prophète (salla allahou alayhi wa sallam) sur le mode alimentaire à suivre durant le mois de ramadan ?<br/>
Les recommandations du prophète au sujet du mois de ramadan concernent tous les volets, spirituel et diététique. En bref, il a conseillé les musulmans de manger léger et de se contenter du peu pour se consacrer aux prières et au Dhikr, qui sont le cœur battant de ce saint mois.</p>

<p>Faire le jeûne durant le mois de ramadan est une expérience unique qui permet au croyant de renforcer sa patience envers les plaisirs de la vie, de se purifier et d’être plus proche d’Allah.
</p>

<h1> La Nuit De Destin (Laylat Al-Qadr) : Quelle Serait Sa Date Exacte ? </h1>
        <img src="photo_coran.png" alt="photo coran"/>

<audio src="sourate_alqadr.mp3" controls> </audio>

<p> Dans le saint Coran, la sourate Al-Qadr divulgue la noblesse et la grandeur d’une nuit qui vaut mieux que mille mois. Une nuit au cours de laquelle les anges du ciel affluent sur la terre pour étendre jusqu’à l’aurore, la bénédiction, la clémence, la grâce et la miséricorde.</p>

<p>Appelée aussi la nuit de la valeur, Laylat Al-qadr jouit d’une grande importance auprès des musulmans du monde, quelles que soient leurs écoles ou doctrines (Madhaheb), car elle correspond à la nuit de la décente du Coran.</p>

<p>Le Coran fut révélé au prophète Mohammed (Salla Allah ‘alayhi wa Salam) une des nuits du mois de ramadan. Allah dit : <strong> « Nous avons fait descendre le Coran dans la nuit de la valeur. Et qui te fera comprendre ce qu’est la nuit de la valeur. La nuit du destin a plus de valeur que mille mois ». </strong> Sourate 97 : 1-3.</p>

<p>Pour déceler la date de cette nuit sacré, nous devons nous appuyer sur les hadiths.</p>

<p>D’après Aïcha (Radia Allah ‘anha), le prophète (Salla Allah ‘alayhi wa Salam) a dit: <strong> » Cherchez la nuit d’Al-Qadr parmi les nuits impaires des derniers jours du mois de ramadan «.</strong></p>

<p>Plusieurs savants, disciples du prophète et notables cheikhs sont en désaccord quant à la date précise de cette nuit, certains comme l’Imam Malek ou al-Shafii prétendent que cette nuit divine ne peut être que le 21 du mois, d’autres tels que l’Imam el-Basri ont dit qu’elle serait la nuit du 25. Mais l’ensemble des recueils et hadiths du prophète semble s’orienter vers la piste de la nuit du 27. Preuve à l’appui, selon Ibn `Omar (Radia Allah ‘anh) : <strong>» Pendant que moi et Ibn al-Abbas comptons nos songes de la dernière décade (du Ramadan) au prophète (Salla Allah ‘alayhi waSalam), il nous a dit : « Je vois que vos songes concordent pour la septième nuit de la dernière décade. Alors que celui qui veut attendre la nuit d’Al-Qadr et la cherche pour profiter de ses mérites, le fasse pendant la 7ème nuit de la dernière décade«</strong> .</p>

<p>Les raisons de la flouté de la date de cette nuit est qu’Allah (sobhanou wa taala) a voulu la cacher afin que les musulmans s’efforcent à bien se comporter durant tout le mois de ramadan et à plonger dans la servitude pour gagner sa miséricorde.</p>

<p>Les mérites de Laylet Al-Qadr sont innombrables, outres le fait d’être la nuit de la descente du Coran et la rencontre du prophète avec l’ange Gabriel, elle représente un moment incontournable pour obtenir la rétribution d’Allah.</p>

